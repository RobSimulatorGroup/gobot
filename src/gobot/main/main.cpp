bool Main::Iteration()
{

    auto window = dynamic_cast<SceneTree*>(OS::GetInstance()->GetMainLoop())->GetRoot()->GetWindowsInterface();

    auto time_now = std::chrono::high_resolution_clock::now();
    auto duration = std::chrono::duration<double, std::ratio<1>>(time_now-s_last_ticks).count();

    s_last_ticks = time_now;

    bool exit = false;
    if (OS::GetInstance()->GetMainLoop()->PhysicsProcess(duration)) {
        exit = true;
    }

    if (OS::GetInstance()->GetMainLoop()->Process(duration)) {
        exit = true;
    }

    // Set view 0 default viewport.
    auto width = window->GetWidth();
    auto height = window->GetHeight();

    LOG_ERROR("width = {}", width);
    LOG_ERROR("height = {}", height);

    bgfx::setViewRect(0, 0, 0, uint16_t(width), uint16_t(height) );

    // This dummy draw call is here to make sure that view 0 is cleared
    // if no other draw calls are submitted to view 0.
    bgfx::touch(0);

//    bgfx::renderFrame();

    // Use debug font to print information about this example.
    bgfx::dbgTextClear();
    bgfx::dbgTextImage(
            std::max<uint16_t>(uint16_t(width /2/8 ), 20)-20
            ,std::max<uint16_t>(uint16_t(height/2/16),  6)-6
            , 40
            , 12
            , s_logo
            , 160
    );
    bgfx::dbgTextPrintf(0, 1, 0x0f, "Color can be changed with ANSI \x1b[9;me\x1b[10;ms\x1b[11;mc\x1b[12;ma\x1b[13;mp\x1b[14;me\x1b[0m code too.");

    bgfx::dbgTextPrintf(80, 1, 0x0f, "\x1b[;0m    \x1b[;1m    \x1b[; 2m    \x1b[; 3m    \x1b[; 4m    \x1b[; 5m    \x1b[; 6m    \x1b[; 7m    \x1b[0m");
    bgfx::dbgTextPrintf(80, 2, 0x0f, "\x1b[;8m    \x1b[;9m    \x1b[;10m    \x1b[;11m    \x1b[;12m    \x1b[;13m    \x1b[;14m    \x1b[;15m    \x1b[0m");

    const bgfx::Stats* stats = bgfx::getStats();
    bgfx::dbgTextPrintf(0, 2, 0x0f, "Backbuffer %dW x %dH in pixels, debug text %dW x %dH in characters."
            , stats->width
            , stats->height
            , stats->textWidth
            , stats->textHeight
    );

    // Advance to next frame. Rendering thread will be kicked to
    // process submitted rendering primitives.
    bgfx::frame();
    bgfx::reset(width, height, 0);

    return exit;

}

void Main::Cleanup() {
    SceneInitializer::Destroy();

    Object::Delete(s_input);
    Object::Delete(s_project_settings);

    bgfx::shutdown();
}

